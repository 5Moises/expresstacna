public class Camiones {
    private int id_Ca;
    private String Placa;
    private String Marca;
    private String Certificado;
    private String Config_vehicular;
    private String Carga_max;

    public int getId_Ca() {
        return id_Ca;
    }

    public void setId_Ca(int id_Ca) {
        this.id_Ca = id_Ca;
    }

    public String getPlaca() {
        return Placa;
    }

    public void setPlaca(String Placa) {
        this.Placa = Placa;
    }

    public String getMarca() {
        return Marca;
    }

    public void setMarca(String Marca) {
        this.Marca = Marca;
    }

    public String getCertificado() {
        return Certificado;
    }

    public void setCertificado(String Certificado) {
        this.Certificado = Certificado;
    }

    public String getConfig_vehicular() {
        return Config_vehicular;
    }

    public void setConfig_vehicular(String Config_vehicular) {
        this.Config_vehicular = Config_vehicular;
    }

    public String getCarga_max() {
        return Carga_max;
    }

    public void setCarga_max(String Carga_max) {
        this.Carga_max = Carga_max;
    }
}
